static const uint32_t k[8] =
{
	0x00000000, 0x5A827999, 0x6ED9EBA1, 0x8F1BBCDC,
	0x50A28BE6, 0x5C4DD124, 0x6D703EF3, 0x00000000,
};

static void loop(const uint8_t *data, int i, uint32_t *tmp, int ofs)
{
	uint32_t f;

	if ((ofs ? 63 - i : i) <= 15)
		f = tmp[1] ^ tmp[2] ^ tmp[3];
	else if ((ofs ? 63 - i : i) <= 31)
		f = (tmp[1] & tmp[2]) | ((~tmp[1]) & tmp[3]);
	else if ((ofs ? 63 - i : i) <= 47)
		f = (tmp[1] | (~tmp[2])) ^ tmp[3];
	else
		f = (tmp[1] & tmp[3]) | (tmp[2] & (~tmp[3]));
	f += k[(ofs ? 64 + i : i) / 16];
	f += tmp[0];
	f += le32dec(&data[g[ofs ? 64 + i : i] * 4]);
	f = rol32(f, s[ofs ? 64 + i : i]);
	tmp[0] = tmp[3];
	tmp[3] = tmp[2];
	tmp[2] = tmp[1];
	tmp[1] = f;
}

static void chunk(struct ripemd256_ctx *ctx, const uint8_t *data)
{
	uint32_t tmp[8];

	for (int i = 0; i < 8; ++i)
		tmp[i] = ctx->h[i];
	for (int i = 0; i < 64; ++i)
	{
		loop(data, i, &tmp[0], 0);
		loop(data, i, &tmp[4], 1);
		if (i == 15)
			swap32(&tmp[0], &tmp[4]);
		else if (i == 31)
			swap32(&tmp[1], &tmp[5]);
		else if (i == 47)
			swap32(&tmp[2], &tmp[6]);
		else if (i == 63)
			swap32(&tmp[3], &tmp[7]);
	}
	for (int i = 0; i < 8; ++i)
		ctx->h[i] += tmp[i];
}

int ripemd256_init(struct ripemd256_ctx *ctx)
{
	ctx->total_size = 0;
	ctx->data_size = 0;
	ctx->h[0] = 0x67452301;
	ctx->h[1] = 0xEFCDAB89;
	ctx->h[2] = 0x98BADCFE;
	ctx->h[3] = 0x10325476;
	ctx->h[4] = 0x76543210;
	ctx->h[5] = 0xFEDCBA98;
	ctx->h[6] = 0x89ABCDEF;
	ctx->h[7] = 0x01234567;
	return 1;
}

int ripemd256_update(struct ripemd256_ctx *ctx, const void *data, size_t size)
{
	HASH_BUFFERIZE(ctx, data, size, 64);
	return 1;
}

int ripemd256_final(uint8_t *md, struct ripemd256_ctx *ctx)
{
	MERKLE_DAMGARD_FINALIZE(ctx, 64, 0);
	for (int i = 0; i < 8; ++i)
		le32enc(&md[i * 4], ctx->h[i]);
	return 1;
}

int ripemd256(const uint8_t *data, size_t size, uint8_t *md)
{
	struct ripemd256_ctx ctx;
	if (!ripemd256_init(&ctx)
	 || !ripemd256_update(&ctx, data, size)
	 || !ripemd256_final(md, &ctx))
		return 0;
	return 1;
}
