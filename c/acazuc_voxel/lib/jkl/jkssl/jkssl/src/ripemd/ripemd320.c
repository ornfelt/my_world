static const uint32_t k[10] =
{
	0x00000000, 0x5A827999, 0x6ED9EBA1, 0x8F1BBCDC, 0xA953FD4E,
	0x50A28BE6, 0x5C4DD124, 0x6D703EF3, 0x7A6D76E9, 0x00000000,
};

static void loop(const uint8_t *data, int i, uint32_t *tmp, int ofs)
{
	uint32_t f;

	if ((ofs ? 79 - i : i) <= 15)
		f = tmp[1] ^ tmp[2] ^ tmp[3];
	else if ((ofs ? 79 - i : i) <= 31)
		f = (tmp[1] & tmp[2]) | ((~tmp[1]) & tmp[3]);
	else if ((ofs ? 79 - i : i) <= 47)
		f = (tmp[1] | (~tmp[2])) ^ tmp[3];
	else if ((ofs ? 79 - i : i) <= 63)
		f = (tmp[1] & tmp[3]) | (tmp[2] & (~tmp[3]));
	else
		f = tmp[1] ^ (tmp[2] | (~tmp[3]));
	f += k[(ofs ? 80 + i : i) / 16];
	f += tmp[0];
	f += le32dec(&data[g[ofs ? 80 + i : i] * 4]);
	f = rol32(f, s[ofs ? 80 + i : i]) + tmp[4];
	tmp[0] = tmp[4];
	tmp[4] = tmp[3];
	tmp[3] = rol32(tmp[2], 10);
	tmp[2] = tmp[1];
	tmp[1] = f;
}

static void chunk(struct ripemd320_ctx *ctx, const uint8_t *data)
{
	uint32_t tmp[10];

	for (int i = 0; i < 10; ++i)
		tmp[i] = ctx->h[i];
	for (int i = 0; i < 80; ++i)
	{
		loop(data, i, &tmp[0], 0);
		loop(data, i, &tmp[5], 1);
		if (i == 15)
			swap32(&tmp[1], &tmp[6]);
		else if (i == 31)
			swap32(&tmp[3], &tmp[8]);
		else if (i == 47)
			swap32(&tmp[0], &tmp[5]);
		else if (i == 63)
			swap32(&tmp[2], &tmp[7]);
		else if (i == 79)
			swap32(&tmp[4], &tmp[9]);
	}
	for (int i = 0; i < 10; ++i)
		ctx->h[i] += tmp[i];
}

int ripemd320_init(struct ripemd320_ctx *ctx)
{
	ctx->total_size = 0;
	ctx->data_size = 0;
	ctx->h[0] = 0x67452301;
	ctx->h[1] = 0xEFCDAB89;
	ctx->h[2] = 0x98BADCFE;
	ctx->h[3] = 0x10325476;
	ctx->h[4] = 0xC3D2E1F0;
	ctx->h[5] = 0x76543210;
	ctx->h[6] = 0xFEDCBA98;
	ctx->h[7] = 0x89ABCDEF;
	ctx->h[8] = 0x01234567;
	ctx->h[9] = 0x3C2D1E0F;
	return 1;
}

int ripemd320_update(struct ripemd320_ctx *ctx, const void *data, size_t size)
{
	HASH_BUFFERIZE(ctx, data, size, 64);
	return 1;
}

int ripemd320_final(uint8_t *md, struct ripemd320_ctx *ctx)
{
	MERKLE_DAMGARD_FINALIZE(ctx, 64, 0);
	for (int i = 0; i < 10; ++i)
		le32enc(&md[i * 4], ctx->h[i]);
	return 1;
}

int ripemd320(const uint8_t *data, size_t size, uint8_t *md)
{
	struct ripemd320_ctx ctx;
	if (!ripemd320_init(&ctx)
	 || !ripemd320_update(&ctx, data, size)
	 || !ripemd320_final(md, &ctx))
		return 0;
	return 1;
}
