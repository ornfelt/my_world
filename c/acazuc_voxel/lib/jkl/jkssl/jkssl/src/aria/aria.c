static void fo(uint8_t *dst, const uint8_t *d, const uint8_t *rk)
{
	uint8_t tmp[16];
	for (size_t i = 0; i < 16; ++i)
		tmp[i] = d[i] ^ rk[i];
	sl1(tmp);
	a(dst, tmp);
}

static void fe(uint8_t *dst, const uint8_t *d, const uint8_t *rk)
{
	uint8_t tmp[16];
	for (size_t i = 0; i < 16; ++i)
		tmp[i] = d[i] ^ rk[i];
	sl2(tmp);
	a(dst, tmp);
}

static int get_rounds_count(struct aria_ctx *ctx)
{
	if (ctx->key_len == 16)
		return 11;
	if (ctx->key_len == 24)
		return 13;
	return 15;
}

void aria_encrypt(struct aria_ctx *ctx, uint8_t *out, const uint8_t *in)
{
	uint8_t block[16];
	memcpy(block, in, 16);
	size_t rounds = get_rounds_count(ctx);
	fo(block, block, ctx->ekeys[0x0]);
	fe(block, block, ctx->ekeys[0x1]);
	fo(block, block, ctx->ekeys[0x2]);
	fe(block, block, ctx->ekeys[0x3]);
	fo(block, block, ctx->ekeys[0x4]);
	fe(block, block, ctx->ekeys[0x5]);
	fo(block, block, ctx->ekeys[0x6]);
	fe(block, block, ctx->ekeys[0x7]);
	fo(block, block, ctx->ekeys[0x8]);
	fe(block, block, ctx->ekeys[0x9]);
	fo(block, block, ctx->ekeys[0xA]);
	if (rounds > 11)
	{
		fe(block, block, ctx->ekeys[0xB]);
		fo(block, block, ctx->ekeys[0xC]);
		if (rounds > 13)
		{
			fe(block, block, ctx->ekeys[0xD]);
			fo(block, block, ctx->ekeys[0xE]);
		}
	}
	for (size_t i = 0; i < 16; ++i)
		block[i] ^= ctx->ekeys[rounds][i];
	sl2(block);
	rounds++;
	memxor(out, block, ctx->ekeys[rounds], 16);
}

