static void rotate_left(uint8_t *dst, uint8_t *src, uint64_t c, uint64_t bytes)
{
	uint64_t bits = bytes * 8;
	for (size_t i = 0; i < bytes; ++i)
	{
		int d = (i * 8 + c) % bits;
		int dm8 = d % 8;
		dst[i] = (src[d / 8] & (0xff >> dm8)) << dm8;
		d = (d + 8) % bits;
		dm8 = 8 - (d % 8);
		dst[i] |= (src[d / 8] & (0xff << dm8)) >> dm8;
	}
}

static void rotate_right(uint8_t *dst, uint8_t *src, uint64_t c, uint64_t bytes)
{
	uint64_t bits = bytes * 8;
	for (size_t i = 0; i < bytes; ++i)
	{
		int d = (i * 8 - c) % bits;
		if (d < 0)
			d += bits;
		int dm8 = d % 8;
		dst[i] = (src[d / 8] & (0xff >> dm8)) << dm8;
		d = (d + 8) % bits;
		if (d < 0)
			d += bits;
		dm8 = 8 - (d % 8);
		dst[i] |= (src[d / 8] & (0xff << dm8)) >> dm8;
	}
}

