static uint64_t fl(uint64_t in1, uint64_t in2)
{
	uint32_t x1 = in1 >> 32;
	uint32_t x2 = in1;
	x2 ^= rol32(x1 & (uint32_t)(in2 >> 32), 1);
	x1 ^= (x2 | (uint32_t)(in2));
	return ((uint64_t)x1 << 32) | x2;
}

static uint64_t flinv(uint64_t in1, uint64_t in2)
{
	uint32_t y1 = in1 >> 32;
	uint32_t y2 = in1;
	y1 = y1 ^ (y2 | (uint32_t)(in2));
	y2 = y2 ^ rol32(y1 & (uint32_t)(in2 >> 32), 1);
	return ((uint64_t)y1 << 32) | y2;
}

static uint64_t f(uint64_t in)
{
	uint8_t t[8];
	t[0] = sbox1[((in >> 56)) & 0xFF];
	t[1] = sbox2[((in >> 48)) & 0xFF];
	t[2] = sbox3[((in >> 40)) & 0xFF];
	t[3] = sbox4[((in >> 32)) & 0xFF];
	t[4] = sbox2[((in >> 24)) & 0xFF];
	t[5] = sbox3[((in >> 16)) & 0xFF];
	t[6] = sbox4[((in >>  8)) & 0xFF];
	t[7] = sbox1[((in >>  0)) & 0xFF];
	uint64_t out = 0;
	out |= (uint64_t)(t[0] ^ t[2] ^ t[3] ^ t[5] ^ t[6] ^ t[7]) << 56;
	out |= (uint64_t)(t[0] ^ t[1] ^ t[3] ^ t[4] ^ t[6] ^ t[7]) << 48;
	out |= (uint64_t)(t[0] ^ t[1] ^ t[2] ^ t[4] ^ t[5] ^ t[7]) << 40;
	out |= (uint64_t)(t[1] ^ t[2] ^ t[3] ^ t[4] ^ t[5] ^ t[6]) << 32;
	out |= (uint64_t)(t[0] ^ t[1] ^ t[5] ^ t[6] ^ t[7]) << 24;
	out |= (uint64_t)(t[1] ^ t[2] ^ t[4] ^ t[6] ^ t[7]) << 16;
	out |= (uint64_t)(t[2] ^ t[3] ^ t[4] ^ t[5] ^ t[7]) << 8;
	out |= (uint64_t)(t[0] ^ t[3] ^ t[4] ^ t[5] ^ t[6]) << 0;
	return out;
}

static void decrypt_loop(struct camellia_ctx *ctx, uint64_t *d1, uint64_t *d2,
                         int i)
{
	if ((ctx->key_len == 16 && i != 2) || (ctx->key_len != 16 && i != 3))
	{
		*d1 = fl(*d1, ctx->ke[i * 2 + 1]);
		*d2 = flinv(*d2, ctx->ke[i * 2 + 0]);
	}
	*d2 ^= f(*d1 ^ ctx->k[i * 6 + 5]);
	*d1 ^= f(*d2 ^ ctx->k[i * 6 + 4]);
	*d2 ^= f(*d1 ^ ctx->k[i * 6 + 3]);
	*d1 ^= f(*d2 ^ ctx->k[i * 6 + 2]);
	*d2 ^= f(*d1 ^ ctx->k[i * 6 + 1]);
	*d1 ^= f(*d2 ^ ctx->k[i * 6 + 0]);
}

void camellia_decrypt(struct camellia_ctx *ctx, uint8_t *out, const uint8_t *in)
{
	uint64_t d1 = be64dec(&in[0]);
	uint64_t d2 = be64dec(&in[8]);
	d1 ^= ctx->kw[2];
	d2 ^= ctx->kw[3];
	if (ctx->key_len != 16)
		decrypt_loop(ctx, &d1, &d2, 3);
	decrypt_loop(ctx, &d1, &d2, 2);
	decrypt_loop(ctx, &d1, &d2, 1);
	decrypt_loop(ctx, &d1, &d2, 0);
	d2 ^= ctx->kw[0];
	d1 ^= ctx->kw[1];
	be64enc(&out[0], d2);
	be64enc(&out[8], d1);
}

static void encrypt_loop(struct camellia_ctx *ctx, uint64_t *d1, uint64_t *d2,
                         int i)
{
	if (i)
	{
		*d1 = fl(*d1, ctx->ke[(i - 1) * 2 + 0]);
		*d2 = flinv(*d2, ctx->ke[(i - 1) * 2 + 1]);
	}
	*d2 ^= f(*d1 ^ ctx->k[i * 6 + 0]);
	*d1 ^= f(*d2 ^ ctx->k[i * 6 + 1]);
	*d2 ^= f(*d1 ^ ctx->k[i * 6 + 2]);
	*d1 ^= f(*d2 ^ ctx->k[i * 6 + 3]);
	*d2 ^= f(*d1 ^ ctx->k[i * 6 + 4]);
	*d1 ^= f(*d2 ^ ctx->k[i * 6 + 5]);
}

void camellia_encrypt(struct camellia_ctx *ctx, uint8_t *out, const uint8_t *in)
{
	uint64_t d1 = be64dec(&in[0]);
	uint64_t d2 = be64dec(&in[8]);
	d1 ^= ctx->kw[0];
	d2 ^= ctx->kw[1];
	encrypt_loop(ctx, &d1, &d2, 0);
	encrypt_loop(ctx, &d1, &d2, 1);
	encrypt_loop(ctx, &d1, &d2, 2);
	if (ctx->key_len != 16)
		encrypt_loop(ctx, &d1, &d2, 3);
	d2 ^= ctx->kw[2];
	d1 ^= ctx->kw[3];
	be64enc(&out[0], d2);
	be64enc(&out[8], d1);
}

static void fill_kl_kr(uint8_t *klr, const uint8_t *key, uint8_t len)
{
	le64enc(&klr[0], be64dec(&key[0]));
	le64enc(&klr[8], be64dec(&key[8]));
	if (len == 16)
	{
		memset(&klr[16], 0, 16);
	}
	else if (len == 24)
	{
		le64enc(&klr[16], be64dec(&key[16]));
		le64enc(&klr[24], ~be64dec(&key[16]));
	}
	else
	{
		le64enc(&klr[16], be64dec(&key[16]));
		le64enc(&klr[24], be64dec(&key[24]));
	}
}

static void fill_ka(uint8_t *kab, uint8_t *klr)
{
	uint64_t d1 = le64dec(&klr[0]) ^ le64dec(&klr[16]);
	uint64_t d2 = le64dec(&klr[8]) ^ le64dec(&klr[24]);
	d2 ^= f(d1 ^ sigma1);
	d1 ^= f(d2 ^ sigma2);
	d1 ^= le64dec(&klr[0]);
	d2 ^= le64dec(&klr[8]);
	d2 ^= f(d1 ^ sigma3);
	d1 ^= f(d2 ^ sigma4);
	le64enc(&kab[0], d1);
	le64enc(&kab[8], d2);
}

static void fill_kb(uint8_t *kab, uint8_t *klr)
{
	uint64_t d1 = le64dec(&kab[0]) ^ le64dec(&klr[16]);
	uint64_t d2 = le64dec(&kab[8]) ^ le64dec(&klr[24]);
	d2 ^= f(d1 ^ sigma5);
	d1 ^= f(d2 ^ sigma6);
	le64enc(&kab[16], d1);
	le64enc(&kab[24], d2);
}

static void rot_key(uint8_t *dst, uint8_t *src, uint8_t rot)
{
	for (size_t i = 0; i < 16; ++i)
	{
		uint8_t a = (16 + i - rot / 8) % 16;
		dst[i] = src[a] << (rot % 8);
		a += 15;
		dst[i] |= src[a % 16] >> (8 - (rot % 8));
	}
}

