void camellia_keyschedule(struct camellia_ctx *ctx, const uint8_t *key,
                          uint8_t len)
{
	uint8_t klr[32];
	uint8_t kab[32];

	fill_kl_kr(klr, key, len);
	fill_ka(kab, klr);
	if (len != 16)
		fill_kb(kab, klr);
	if (len == 16)
		keyschedule_128(ctx, klr, kab);
	else
		keyschedule_192_256(ctx, klr, kab);
	ctx->key_len = len;
}
